qta = 0
qte = 0
qti = 0
palavra = input("Digite uma palavra qualquer : ")

for x in palavra:
    if x == "a":
        qta = qta+1
    if x == "e":
        qte = qte+1
    if x == "i":
        qti = qti+1


print("Quantidade de letras 'a'",qta)
print("Quantidade de letras 'a'",qte)
print("Quantidade de letras 'a'",qti)

        
