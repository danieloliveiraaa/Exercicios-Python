frase = input("Digite uma frase: ")

tam=len(frase)

qta=frase.count("a")
qtm=frase.count("m")
qtc=frase.count("c")
qtp=frase.count("p")
qtf=frase.count("f")

print("Tamanho da frase digitada", tam)
print("Quantidade de letras A",qta)
print("Quantidade de letras M",qtm)
print("Quantidade de letras C",qtc)
print("Quantidade de letras P",qtp)
print("Quantidade de letras F",qtf)
